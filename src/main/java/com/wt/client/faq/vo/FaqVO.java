package com.wt.client.faq.vo;

public class FaqVO {
	private int f_num; // 자주묻는질문 번호
	private String f_question; // 자주묻는질문 질문
	private String f_answer; // 자주묻는질문 답변

	public int getF_num() {
		return f_num;
	}

	public void setF_num(int f_num) {
		this.f_num = f_num;
	}

	public String getF_question() {
		return f_question;
	}

	public void setF_question(String f_question) {
		this.f_question = f_question;
	}

	public String getF_answer() {
		return f_answer;
	}

	public void setF_answer(String f_answer) {
		this.f_answer = f_answer;
	}

}
