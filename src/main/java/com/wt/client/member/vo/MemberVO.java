package com.wt.client.member.vo;

import java.util.Date;

public class MemberVO {
	
	private String c_name;
	private String c_id;
	private String c_pwd;
	private String c_regDate;
	private String c_email;
	private String c_phone;
	private String c_disabled;
	
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public String getC_pwd() {
		return c_pwd;
	}
	public void setC_pwd(String c_pwd) {
		this.c_pwd = c_pwd;
	}
	
	public String getC_regDate() {
		return c_regDate;
	}
	public void setC_regDate(String c_regDate) {
		this.c_regDate = c_regDate;
	}
	public String getC_disabled() {
		return c_disabled;
	}
	public void setC_disabled(String c_disabled) {
		this.c_disabled = c_disabled;
	}
	public String getC_email() {
		return c_email;
	}
	public void setC_email(String c_email) {
		this.c_email = c_email;
	}
	public String getC_phone() {
		return c_phone;
	}
	public void setC_phone(String c_phone) {
		this.c_phone = c_phone;
	}
	
}

